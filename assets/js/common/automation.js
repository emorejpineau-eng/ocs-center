let workflows = [];

function createWorkflow() {

  const trigger = document.getElementById("trigger").value;
  const action = document.getElementById("action").value;

  const wf = {
    id: Date.now(),
    trigger,
    action,
    status: "active"
  };

  workflows.push(wf);

  renderWorkflows();
  pushLog(`Workflow created: ${trigger} → ${action}`);
}

function renderWorkflows() {

  const list = document.getElementById("workflowList");

  list.innerHTML = workflows.map(w => `
    <div style="
      padding:10px;
      margin-bottom:8px;
      background:#0B1220;
      border-radius:8px;
    ">
      ⚙️ ${w.trigger} → ${w.action}
      <span style="color:#00E676;">[${w.status}]</span>
    </div>
  `).join("");
}

function simulateEvents() {

  const events = [
    "login",
    "api",
    "security",
    "user"
  ];

  setInterval(() => {

    const event = events[Math.floor(Math.random() * events.length)];

    const matched = workflows.filter(w => w.trigger === event);

    matched.forEach(w => {
      executeWorkflow(w, event);
    });

  }, 4000);
}

function executeWorkflow(workflow, event) {

  const msg = `[EXEC] ${workflow.trigger} triggered → ${workflow.action}`;

  pushLog(msg);

  if (workflow.action === "notify") {
    pushNotification(msg, "info");
  }

  if (workflow.action === "block") {
    pushNotification("Action BLOCK executed", "danger");
  }

  if (workflow.action === "report") {
    pushNotification("Report generated by SMART-IA", "success");
  }
}

function pushLog(msg) {
  const box = document.getElementById("autoLogs");

  box.innerHTML += msg + "<br>";
}

simulateEvents();

